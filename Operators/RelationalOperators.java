public class RelationalOperators {
    public static void main(String[] args) {
        // Relational operators --> == , != , < , > , <= , >=
        int a = 10;
        int b = 35;

        boolean c = (a == b); // false
         System.out.println(c);

         boolean d = (a != b); // true
         System.out.println(d);

         boolean e = (a < b); // true
         System.out.println(e);

         boolean f = (a <= b);// true
         System.out.println(f);
    }
}
